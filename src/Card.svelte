      <h2 class="card-heading">Details</h2>

      <p>
        The Jack Herer Fast strain is a 75% sativa-dominant strain. You will find it resistant to most diseases if grown in correctly. This strain produces some pretty good yields. As a novice grower I was able to obtain about 4oz or 120grams per plant. 
      </p>
 
        <h3 class="detail-heading">Veg</h3>
      <p>
        When grown indoors from seed you can expect the plant to reach between 90 - 100 cm  tall, that's about 3ft for those of us from the old school. If you give the strain a little less blue light and more red in the last few weeks of veg or during the transition to flower you can will reveal more of the sativa dominat charachterisics from the plant. Depending on how you grow you can expect the strain to be in veg for 2 months assuming your using common low/high stress training techiniques. You can flip it to veg earlier at about week six if your not training your plants.    
      </p>
      <h3 class="detail-heading">Flower</h3>
      <p>Though you will find that the flowering period for this strain is listed at 8 to 9 weeks you might find that you will benifit from another month. Growers find that the transition to 70% cloudy trichomes isn't completed unitl later in the flowering process. So give yourself some extra time and let it slowly rippen</p>

      <h3 class="detail-heading">Potency</h3>
